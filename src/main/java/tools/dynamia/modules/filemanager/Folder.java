package tools.dynamia.modules.filemanager;

public class Folder {

    private String name = "";
    private String parent = "";

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getParent() {
        return parent;
    }

    public void setParent(String parent) {
        this.parent = parent;
    }

}
